func TestCreateManualHappyPath(t *testing.T) {
	svc, q, _ := newTestService(t)
	ctx := context.Background()
	c := createCheck(t, q, "api")

	inc, err := svc.CreateManual(ctx, CreateIncidentInput{
		Title:            "Manual problem",
		Severity:         SeverityMinor,
		InitialMessage:   "Kicking off investigation",
		AffectedCheckIDs: []int64{c.ID},
	})
	if err != nil {
		t.Fatalf("create manual: %v", err)
	}
	if inc.ID == 0 {
		t.Fatal("incident not assigned an ID")
	}
	if inc.Status != string(StatusInvestigating) {
		t.Fatalf("expected investigating, got %s", inc.Status)
	}
	if inc.AutoCreated {
		t.Fatal("manual incident should not be auto_created")
	}

	updates, err := q.ListUpdatesForIncident(ctx, inc.ID)
	if err != nil {
		t.Fatalf("list updates: %v", err)
	}
	if len(updates) != 1 {
		t.Fatalf("expected 1 update, got %d", len(updates))
	}
	if updates[0].AutoGenerated {
		t.Fatal("initial manual update should not be auto_generated")
	}

	ids, err := q.ListAffectedCheckIDsForIncident(ctx, inc.ID)
	if err != nil {
		t.Fatalf("list affected: %v", err)
	}
	if len(ids) != 1 || ids[0] != c.ID {
		t.Fatalf("expected [%d], got %v", c.ID, ids)
	}
}

