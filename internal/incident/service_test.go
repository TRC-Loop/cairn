func TestPostUpdateSameStatusKeepsStatus(t *testing.T) {
	svc, q, _ := newTestService(t)
	ctx := context.Background()
	c := createCheck(t, q, "api")
	id, err := svc.CreateAutoFromCheckFailure(ctx, c)
	if err != nil {
		t.Fatalf("auto: %v", err)
	}
	investigating := StatusInvestigating
	upd, err := svc.PostUpdate(ctx, PostUpdateInput{
		IncidentID: id,
		Message:    "still investigating",
		NewStatus:  &investigating,
	})
	if err != nil {
		t.Fatalf("post update: %v", err)
	}
	if upd.Status != string(StatusInvestigating) {
		t.Fatalf("update status = %s", upd.Status)
	}
	inc, _ := q.GetIncident(ctx, id)
	if inc.Status != string(StatusInvestigating) {
		t.Fatalf("incident status = %s", inc.Status)
	}
	if inc.ResolvedAt.Valid {
		t.Fatal("resolved_at should remain unset")
	}
	updates, _ := q.ListUpdatesForIncident(ctx, id)
	if len(updates) != 2 {
		t.Fatalf("expected 2 updates (auto + same-state), got %d", len(updates))
	}
}

func resolveAndBackdate(t *testing.T, svc *Service, db *sql.DB, ctx context.Context, id int64, ago time.Duration) {
	t.Helper()
	if err := svc.Transition(ctx, id, StatusResolved, nil, "fixed"); err != nil {
		t.Fatalf("resolve: %v", err)
	}
	at := time.Now().UTC().Add(-ago)
	if _, err := db.ExecContext(ctx, "UPDATE incidents SET resolved_at = ? WHERE id = ?", at, id); err != nil {
		t.Fatalf("backdate: %v", err)
	}
}

