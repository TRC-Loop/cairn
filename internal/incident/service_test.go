func TestReopenFlappingHighDownReopens(t *testing.T) {
	svc, q, db := newTestService(t)
	ctx := context.Background()
	c := createCheck(t, q, "api")
	setReopenMode(t, db, "flapping_only", 3600)
	id1, err := svc.CreateAutoFromCheckFailure(ctx, c)
	if err != nil {
		t.Fatalf("auto: %v", err)
	}
	resolveAndBackdate(t, svc, db, ctx, id1, 5*time.Minute)
	now := time.Now().UTC()
	for i := 0; i < 6; i++ {
		_ = q.InsertCheckResult(ctx, store.InsertCheckResultParams{CheckID: c.ID, CheckedAt: now.Add(-time.Duration(i) * time.Minute), Status: "down"})
	}
	for i := 0; i < 2; i++ {
		_ = q.InsertCheckResult(ctx, store.InsertCheckResultParams{CheckID: c.ID, CheckedAt: now.Add(-time.Duration(i+10) * time.Second), Status: "up"})
	}
	id2, err := svc.CreateAutoFromCheckFailure(ctx, c)
	if err != nil {
		t.Fatalf("auto2: %v", err)
	}
	if id1 != id2 {
		t.Fatalf("expected reopen, got new %d", id2)
	}
}

